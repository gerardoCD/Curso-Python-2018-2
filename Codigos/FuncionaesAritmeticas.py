def Suma(numero1,numero2):
	"""La función Suma elabora una operacion aritmetica de suma ingresando dos numeros como parametro y retornando su resultado de la opereación"""
	return numero1 + numero2
def Resta(numero1,numero2):
	return numero1 - numero2
def Multiplicacion(numero1,numero2):
	return numero1 * numero2
